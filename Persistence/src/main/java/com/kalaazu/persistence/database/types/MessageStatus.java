package com.kalaazu.persistence.database.types;

/**
 * Message status type.
 * ====================
 *
 * Represents the status of the message.
 *
 * 0 = Unread.
 * 1 = Read.
 * 2 = Deleted.
 *
 * @author Manulaiko <manulaiko@gmail.com>
 */
public enum MessageStatus {
    UNREAD,
    READ,
    DELETED
}
